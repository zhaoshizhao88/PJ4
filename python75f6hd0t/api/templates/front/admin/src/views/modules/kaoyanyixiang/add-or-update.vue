						{ validator: validateNumber, trigger: 'blur' },
					],
					suozaidi: [
					],
					kexuandi: [
					],
					dengjishijian: [
					],
					yixiangpianhao: [
					],
					yixiangxiangqing: [
					],
				},
			};
		},
		props: ["parent"],
		computed: {



		},
		components: {
		},
		created() {
		},
		methods: {
			// 下载
			download(file){
				window.open(`${file}`)
			},
			// 初始化
			init(id,type) {
				if (id) {
					this.id = id;
					this.type = type;
				}
				if(this.type=='info'||this.type=='else'||this.type=='msg'){
					this.info(id);
				}else if(this.type=='logistics'){
					for(let x in this.ro) {
						this.ro[x] = true
					}
					this.logistics=false;
					this.info(id);
				}else if(this.type=='cross'){
					var obj = this.$storage.getObj('crossObj');
					for (var o in obj){
						if(o=='zhanghao'){
							this.ruleForm.zhanghao = obj[o];
							this.ro.zhanghao = true;
							continue;
						}
						if(o=='xingming'){
							this.ruleForm.xingming = obj[o];
							this.ro.xingming = true;
							continue;
						}
						if(o=='xingbie'){
							this.ruleForm.xingbie = obj[o];
							this.ro.xingbie = true;
							continue;
						}
						if(o=='touxiang'){
							this.ruleForm.touxiang = obj[o];
							this.ro.touxiang = true;
							continue;
						}
						if(o=='xueli'){
							this.ruleForm.xueli = obj[o];
							this.ro.xueli = true;
							continue;
						}
						if(o=='zhuanye'){
							this.ruleForm.zhuanye = obj[o];
							this.ro.zhuanye = true;
							continue;
						}
						if(o=='yixiangcengci'){
							this.ruleForm.yixiangcengci = obj[o];
							this.ro.yixiangcengci = true;
							continue;
						}
						if(o=='chengjifenshu'){
							this.ruleForm.chengjifenshu = obj[o];
							this.ro.chengjifenshu = true;
							continue;
						}
						if(o=='suozaidi'){
							this.ruleForm.suozaidi = obj[o];
							this.ro.suozaidi = true;
							continue;
